use std::{env, fs, path::PathBuf};

fn main() {
  let manifest_dir = PathBuf::from(env::var("CARGO_MANIFEST_DIR").expect("missing manifest dir"));
  let doc_samples_dir = manifest_dir.join("doc_samples");
  let out_dir = PathBuf::from(env::var("OUT_DIR").expect("missing OUT_DIR"));
  let out_file = out_dir.join("doc_samples_tests.rs");

  println!("cargo:rerun-if-changed={}", doc_samples_dir.display());

  let mut samples = Vec::new();
  for entry in fs::read_dir(&doc_samples_dir).expect("read doc_samples dir") {
    let entry = entry.expect("read doc_samples entry");
    let path = entry.path();
    if path.is_file() {
      println!("cargo:rerun-if-changed={}", path.display());
      samples.push(entry.file_name().to_string_lossy().into_owned());
    }
  }
  samples.sort();

  let mut generated = String::from("// @generated by build.rs\n\n");
  for file_name in samples {
    let slug = slugify(&file_name);
    let test_name = format!("doc_sample_{slug}");
    let version_cfg = version_cfg_attr(&file_name);

    if is_invalid(&file_name) {
      if !version_cfg.is_empty() {
        generated.push_str(&version_cfg);
      }
      generated.push_str(&format!(
        "#[test]\nfn invalid_{test_name}() {{\n  assert_doc_sample_invalid({file_name:?});\n}}\n\n"
      ));
      continue;
    }

    if is_valid_open_only(&file_name) {
      if !version_cfg.is_empty() {
        generated.push_str(&version_cfg);
      }
      generated.push_str(&format!(
        "#[test]\nfn open_valid_{test_name}() {{\n  assert_doc_sample_opens({file_name:?});\n}}\n\n"
      ));
      continue;
    }

    if !is_round_trip_supported(&file_name) {
      if !version_cfg.is_empty() {
        generated.push_str(&version_cfg);
      }
      generated.push_str(&format!(
        "#[test]\nfn open_{test_name}() {{\n  assert_doc_sample_opens({file_name:?});\n}}\n\n"
      ));
      continue;
    }

    if !version_cfg.is_empty() {
      generated.push_str(&version_cfg);
    }

    generated.push_str(&format!(
      "#[test]\nfn round_trip_{test_name}() {{\n  assert_doc_sample_round_trip({file_name:?});\n}}\n\n"
    ));
  }

  fs::write(out_file, generated).expect("write generated doc sample tests");
}

